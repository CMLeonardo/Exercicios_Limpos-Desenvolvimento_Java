package fundamentos;

public class AreaCircunferencia {
	public static void main(String[] args) {
		final double PI   = 3.14159; // deixando o pi constante || final diz que é uma constante
			  double raio = 3;
		
			  double area = PI * raio * raio;
		
		System.out.println("Área = " + area + "m2."); 
	}
}
